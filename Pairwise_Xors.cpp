void even(int x)
{
    int setBits = __builtin_popcountll(x);
    if (setBits == 1)
    {
        cout << -1;
        return;
    }
    if ((!((x - 2) % 4)))
    {
        cout << 0 << " " << 1 << " " << (x / 2) ;
        return;
    }
    else
    {
        int a = 0, b = x / 2;
        int c = 1;
        while (((a ^ b) + (b ^ c) + (c ^ a)) != x)
            c *= 2;
        cout << a << " " << b << " " << c;
        return;
    }
}

